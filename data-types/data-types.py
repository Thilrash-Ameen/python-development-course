# Built-in Data types

# Getting the data type
x = 5
print(type(x))

# Setting the data types

# str
x = "Hello World"
# int
x = 20
# float
x = 20.15
# complex
x = 1j
# list
x = ["apple", "banana", "cherry"]
# tuple
x = ("apple", "banana", "cherry")
# range
x = range(6)
# dict
x = {"name" : "Jhon", "age" : 36}
# set
x = {"apple", "banana", "cherry"}
# frozenset
x = frozenset({"apple", "banana", "cherry"})
# boolean
x = True
# bytes
x = b"Hello"
# bytearray
x = bytearray(5)
# memoryview
x = memoryview(bytes(5))
