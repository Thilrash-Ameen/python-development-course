# Python Variables
# These are containers for storing data values

# Creating variables
x = 5
y = "Jhon"

print(x)
print(y)

# Variable do not need to be declared with any particular type and even change type after they have been set

# x set to String from integer
x = "Sam"
# x set to Integer from String again
x = 10

print(x)

# Casting

# If you want to specify the data type of a variable, this can be done with casting.
x = str(3)
y = int(3)
z = float(3)

print(x)
print(y)
print(z)

# Get the type
x = 5
y = "Jhon"

print(type(x))
print(type(y))

# String variables can be declared either by using single or double quotes
name = "Thilrash"
fullName = 'Thilrash Ameen'

print(name)
print(fullName)

# Case Sensitive

# Variable names are case sensitive
a = 4
A = 10

print(a)
print(A)
